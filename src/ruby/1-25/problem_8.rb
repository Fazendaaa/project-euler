#!/usr/bin/ruby

def largest_product_series( series, digits )
    max = 0

    for i in 0..series.length do
        if series.length-1 >= i + digits-1 then
            temp = 1
            for j in i..i+digits-1 do
                temp = temp * series[ j ]
            end

            if temp > max then
                max = temp
                index = i
            end
        else
            break
        end
    end
    
    return series[ index..index+digits-1 ]
end

